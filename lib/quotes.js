export function quoteOfDay(date = new Date()) {
  const dayNumber = Math.floor(
    Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()) / 86400000
  );
  const idx = ((dayNumber % QUOTES.length) + QUOTES.length) % QUOTES.length;
  return QUOTES[idx];
}
